-- ========================
-- DROP (옵션)
-- ========================
BEGIN
    FOR t IN (SELECT table_name FROM user_tables) LOOP
        EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
    END LOOP;
END;
/

-- ========================
-- 1. 기본 테이블 (참조 대상 먼저)
-- ========================

CREATE TABLE USERS (
    USER_NO NUMBER PRIMARY KEY,
    USER_NAME VARCHAR2(50) NOT NULL,
    EMAIL VARCHAR2(100),
    PHONE VARCHAR2(50),
    PROVIDER VARCHAR2(50),
    CREATED_AT DATE NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    DELETE_AT DATE,
    AUTH NUMBER NOT NULL
);

CREATE TABLE STOCKS (
    STOCK_CODE VARCHAR2(8) PRIMARY KEY,
    STOCK_NAME VARCHAR2(100) NOT NULL,
    MARKET_TYPE VARCHAR2(20),
    SECTOR VARCHAR2(50),
    LISTED_DATE DATE,
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE'
);

-- ========================
-- 2. 종속 테이블
-- ========================

CREATE TABLE ACCOUNTS (
    ACCOUNT_NO NUMBER PRIMARY KEY,
    USER_NO NUMBER NOT NULL,
    INITIAL_BALANCE NUMBER,
    CREATED_AT DATE,
    STATUS VARCHAR2(15) DEFAULT 'ACTIVE' NOT NULL
);

CREATE TABLE ACCOUNT_BALANCE (
    ACCOUNT_NO NUMBER PRIMARY KEY,
    AVAILABLE_CASH NUMBER,
    LOCKED_CASH NUMBER
);

CREATE TABLE STOCKS_HELD (
    STOCKS_HELD_NO NUMBER PRIMARY KEY,
    ACCOUNT_NO NUMBER NOT NULL,
    STOCK_CODE VARCHAR2(8) NOT NULL,
    STOCK_RESERVES NUMBER,
    AVG_PRICE NUMBER,
    CONSTRAINT UQ_ACC_STOCK UNIQUE (ACCOUNT_NO, STOCK_CODE)
);

CREATE TABLE BOARD (
    BOARD_NO NUMBER PRIMARY KEY,
    USER_NO NUMBER NOT NULL,
    CONTENT VARCHAR2(2000) NOT NULL,
    CREATED_AT DATE NOT NULL,
    LIKE_COUNT NUMBER,
    PARENT_ID NUMBER,
    STOCK_CODE VARCHAR2(8) NOT NULL
);

CREATE TABLE BOARD_LIKE (
    BOARD_NO NUMBER,
    USER_NO NUMBER
);

CREATE TABLE ORDERS (
    ORDER_ID NUMBER PRIMARY KEY,
    ORDER_KIND VARCHAR2(5) NOT NULL,
    ORDER_TYPE VARCHAR2(7) NOT NULL,
    PRICE NUMBER NOT NULL,
    QUANTITY NUMBER NOT NULL,
    STATUS VARCHAR2(10) DEFAULT 'PENDING' NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL,
    USER_NO NUMBER NOT NULL,
    STOCK_CODE VARCHAR2(8) NOT NULL,
    CHECK (ORDER_KIND IN ('BUY','SELL')),
    CHECK (ORDER_TYPE IN ('MARKET','LIMIT'))
);

CREATE TABLE TRADES (
    TRADE_ID NUMBER PRIMARY KEY,
    ORDER_ID NUMBER NOT NULL,
    PRICE NUMBER NOT NULL,
    QUANTITY NUMBER NOT NULL,
    EXECUTED_AT TIMESTAMP NOT NULL
);

CREATE TABLE WATCHLIST (
    USER_NO NUMBER,
    STOCK_CODE VARCHAR2(8),
    PRIMARY KEY (USER_NO, STOCK_CODE)
);

CREATE TABLE NEWS (
    NEWS_ID NUMBER PRIMARY KEY,
    NEWS_TITLE VARCHAR2(500) NOT NULL,
    PUBLISHER VARCHAR2(100) NOT NULL,
    PUBLISHED_AT DATE NOT NULL
);

CREATE TABLE NEWS_STOCK (
    NEWS_ID NUMBER,
    STOCK_CODE VARCHAR2(8),
    PRIMARY KEY (NEWS_ID, STOCK_CODE)
);

CREATE TABLE USER_LOCAL (
    USER_NO NUMBER,
    USER_ID VARCHAR2(50) UNIQUE,
    PASSWORD VARCHAR2(100)
);

CREATE TABLE USER_SOCIAL (
    SOCIAL_NO NUMBER PRIMARY KEY,
    PROVIDER_ID VARCHAR2(100) NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    USER_NO NUMBER
);

CREATE TABLE STOCK_PRICE_TICK (
    TICK_ID NUMBER PRIMARY KEY,
    STOCK_CODE VARCHAR2(8),
    PRICE NUMBER,
    VOLUME NUMBER,
    TIMESTAMP TIMESTAMP
);

CREATE TABLE STOCK_ANALYSIS (
    ANALYSIS_ID NUMBER PRIMARY KEY,
    STOCK_CODE VARCHAR2(8),
    SIGNAL VARCHAR2(5),
    CONFIDENCE NUMBER,
    CREATED_AT DATE
);

CREATE TABLE RECENT_VIEW (
    USER_NO NUMBER,
    STOCK_CODE VARCHAR2(8),
    VIEWED_AT TIMESTAMP
);

CREATE TABLE co_information (
    CORP_CODE VARCHAR2(10) PRIMARY KEY,
    STOCK_CODE VARCHAR2(8),
    CO_NAME VARCHAR2(100),

    ISSUED_STOCK NUMBER,
    DECLINED_STOCK NUMBER,
    TREASURY_STOCK NUMBER,
    OUTSTANDING_SHARES NUMBER,

    MINORITY_SHAREHOLDER_RATIO NUMBER(5,2),
    MINORITY_OWNERSHIP_RATIO NUMBER(5,2),

    UPDATED_AT DATE
);

CREATE TABLE NOTICE (
    NOTICE_ID NUMBER PRIMARY KEY,
    TITLE VARCHAR2(255),
    CONTENT VARCHAR2(2000),
    CREATED_AT DATE,
    UPDATED_AT DATE,
    USER_NO NUMBER
);

CREATE TABLE INVESTMENT_TYPE (
	USER_NO	NUMBER		NOT NULL,
	TOTAL_POINT	NUMBER		NULL,
	RESULT_FILE	VARCHAR2(500)		NULL
);

CREATE TABLE stock_realtime (
	STOCK_REALTIME_ID	NUMBER		NOT NULL,
	STOCK_CODE	VARCHAR2(8)		NOT NULL,
	CURRENT_PRICE	NUMBER		NULL,
	OPEN_PRICE	NUMBER		NULL,
	CHANGE_PRICE	NUMBER		NULL,
	CHANGE_RATE	NUMBER		NULL,
	VOLUME	NUMBER		NULL,
	UPDATED_AT	TIMESTAMP		NULL
