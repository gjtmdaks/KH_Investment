    LOW_PRICE          NUMBER         NULL,
    CLOSE_PRICE        NUMBER         NULL,

    VOLUME             NUMBER         NULL,
    CHANGE_PRICE       NUMBER         NULL,
    CHANGE_RATE        NUMBER         NULL,

